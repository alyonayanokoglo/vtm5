/**
 * @param {string} ruLabel
 * @returns {PredatorRef | null}
 */
export function getPredatorReference(ruLabel) {
  if (!ruLabel || typeof ruLabel !== 'string') return null
  return PREDATOR_REFERENCE_BY_RU[ruLabel.trim()] ?? null
}
